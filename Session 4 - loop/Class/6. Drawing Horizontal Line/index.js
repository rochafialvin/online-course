// Mempraktikkan menjumlahkan data baru pada variabel
//////////////////////////////////////////////////////

// Number berisi string kosong
var number = ''
// Setup loop untuk 5 kali puataran
for(var i = 0; i < 5; i++){
    // Setiap loop akan menjumlahkan nilai number sebelumnya dengan nilai i
    number += i

}

console.log(number);

