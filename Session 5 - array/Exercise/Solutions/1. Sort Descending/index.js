// Result : [ 'Elena', 'Dany', 'Chaplin', 'Bernard', 'Alex' ]
var names = ["Alex", "Elena", "Chaplin", "Bernard", "Dany"]

// [ 'Alex', 'Bernard', 'Chaplin', 'Dany', 'Elena' ]
names.sort()
// [ 'Elena', 'Dany', 'Chaplin', 'Bernard', 'Alex' ]
names.reverse()

