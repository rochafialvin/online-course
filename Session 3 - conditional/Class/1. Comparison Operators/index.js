// Operator Perbandingan
// Menghasilkan nilai boolean (true atau false)

var result = 5 > 4
console.log(result)

console.log(5 > 4)
console.log(5 < 4)

// Sama dengan
console.log(8 == 8)
console.log(8 == 9);

// Tidak sama dengan
console.log(8 != 8)
console.log(8 != 9);

// Hanya membandingkan nilai
console.log(10 == "10")
// Membandingkan nilai dan tipe data
console.log(10 === "10")
console.log(10 === 10);

// Boleh mengikuti ujian jika membawa pensil atau pulpen
// Membawa pulpen tapi tidak membawa pensil : boleh
// Tidak membawa pulpen tapi membawa pensil : boleh
// Tidak membawa pulpen dan pensil : tidak boleh

// Lebih besar atau sama dengan
// 7 > 8 ? false
// 7 == 8 ? false
// false atau false
// false
console.log(7 >= 8)

// 7 > 7 ? false
// 7 == 7 ? true
// false atau true
// true
console.log(7 >= 7)

// 8 > 7 ? true
// 8 == 7 ? false
// true atau false
// true
console.log(8 >= 7)

