// true
console.log(!false)

// true or !true ==> true or false : true
console.log( (6 < 9) || !(7 > 6 ) )
// !false and true ==> true and true : true
console.log( !(3 >= 4) && ( 5 == "5" ) )
// false or false = false
console.log( (3 >= 4) || ( 5 === "5" ) )
// !true and !true ==> false and false : false
console.log( !(6 < 9) && !(7 > 6 ) )
